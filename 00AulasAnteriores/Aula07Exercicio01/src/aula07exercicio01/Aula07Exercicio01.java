
package aula07exercicio01;

public class Aula07Exercicio01 {

    public static void main(String[] args) {
      int n = 4;
      System.out.println("O valor da variável n é " + n++);
      System.out.println("O valor da variável depois do pós incremento é " + n);
    
      int x = 4;
      System.out.println("O valor da variável x é " + ++x);
      System.out.println("O valor da variável depois do pré incremento é " + x);
    
    }
    
}
